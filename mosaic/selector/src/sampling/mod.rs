//! Sampling module for choosing clients participation.
//! 
//! # Simple random sample (or SRS):
//!     A subset of individuals (a sample) chosen from a larger set (a population) 
//!     in which a subset of individuals are chosen randomly, all with the same probability. 
//!     It is a process of selecting a sample in a random way.
//! 
//! # Reservoir sampling 
//!     A family of randomized algorithms for choosing a simple random sample, 
//!     without replacement, of k items from a population of unknown size n 
//!     in a single pass over the items.